
// This script checks if the user is coming from a Netlify Identity confirmation
// link and redirects to the correct admin page.

if (window.netlifyIdentity) {
  window.netlifyIdentity.on("init", user => {
    if (!user) {
      window.netlifyIdentity.on("login", () => {
        document.location.href = "/admin/";
      });
    }
  });
}
